import java.util.*;
class OddEvenNumber {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        System.out.println("enter the x value");
        int x = scan.nextInt();
        if (x % 2 == 0) {
            System.out.println("x is the even number");
        }
        else{
                System.out.println("x is the odd number");
            }
        }
    }
